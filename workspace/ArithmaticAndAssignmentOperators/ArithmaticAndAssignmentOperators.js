"use strict";

console.log("Arithmetic Operators");

let x = 20;
let y = 6;

console.log("The value of x : ", x);
console.log("The value of y : ", y);

console.log("The sum of a x and y : ", x+y);

var concatenatedString = "We" + "are" + "learning" + "JS";
console.log("Concatenated String = ", concatenatedString);

console.log("Substraction of y from x = ", x - y);
console.log("product of x and y = ", x * y);
console.log("Division of x and y = ", x / y);
console.log("Modulus of x and y = ", x % y);

console.log("The value of x : ", x);
console.log("Printing out x++ : ", x++);

console.log("The value of x : ", x);
console.log("Printing out ++x : ", ++x);

console.log("The value of x : ", x);
console.log("Printing out x-- : ", x--);

console.log("The value of x : ", x);
console.log("Printing out --x : ", --x);

console.log("******Assignment Operators******");

x = 20;
y = 6;

console.log("The value of x : ", x);
console.log("The value of y : ", y);

y = x;
console.log("The value of x is assigned to y, so y = ", y);

x += 1;
console.log("Value of x after performing x += 1 is ", x);

y -= 1;
console.log("Value of x after performing y -= 1 is ", y);

